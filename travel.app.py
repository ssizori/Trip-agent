col_h.metric(label="🏨 총 숙박비", value=f"{hotel_cost_total} 만원", delta=f"1박당 {target_base['1일숙박비']}만 원")
col_m.metric(label="🍳 총 식비 및 케어 비용", value=f"{food_cost_total} 만원", delta=f"1일당 {target_base['1일식비']}만 원")
col_e.metric(label="🚌 현지 교통 및 비상 여비", value=f"{etc_transport_cost} 만원", delta="일일 예비 인덱스 연동")

st.info(f"💳 **[{top_1_destination}] {trip_duration} 예상 종합 경비:** 약 **{total_budget_calculated} 만원** 내외")

