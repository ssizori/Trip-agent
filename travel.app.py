# [Part 3] 예산 기반 경비 실시간 추정 계산기
# ==========================================
st.markdown("---")
st.subheader(f"💰 Part 3. [{top_1_destination}] 예산 소요 매트릭스 시뮬레이터")

target_base = dest_base_scores[top_1_destination]
flight_cost = current_month_prices[top_1_destination] 
hotel_cost_total = target_base["1일숙박비"] * total_nights
food_cost_total = target_base["1일식비"] * total_days
