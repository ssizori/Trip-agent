with col_map:
    st.markdown(f"##### 🗺️ [{top_1_destination}] 실시간 스팟 맵 (컨셉: {selected_concept})")
    
    map_df = active_routing["map_data"]
    sliced_map_data = map_df.head(min(total_days, len(map_df)))
    
