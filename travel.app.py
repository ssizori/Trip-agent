trip_duration = st.sidebar.selectbox("📆 여행 일정(박/일) 선택", ["2박 3일", "3박 4일", "4박 5일"], index=1)

# ★[신규 스펙] 희망 여행 총 예산 상한선 필터 옵션 추가
user_budget_tier = st.sidebar.selectbox(
    "💰 희망 여행 예산대 선택 (총액 기준)",
    ["가성비 중심 (100만원 이하)", "적정 예산 (100만원 ~ 200만원)", "프리미엄·유럽형 (200만원 이상)"],
    index=1
)

duration_map = {"2박 3일": 3, "3박 4일": 4, "4박 5일": 5}
total_days = duration_map[trip_duration]
total_nights = total_days - 1

st.sidebar.markdown("---")
st.sidebar.header("⚖️ 2. 추천 알고리즘 가중치 설정")
w_relax = st.sidebar.slider("🏖️ 휴양 비중 (Relax)", 0, 10, 5)
w_sight = st.sidebar.slider("🏛️ 관광 비중 (Sight)", 0, 10, 5)
w_food = st.sidebar.slider("🍳 음식 비중 (Gourmet)", 0, 10, 5)
w_shop = st.sidebar.slider("🛍️ 쇼핑 비중 (Shopping)", 0, 10, 5)
# ★[신규 스펙] 예산 비중 가중치 슬라이더 엔진 연동
w_budget = st.sidebar.slider("🪙 예산 세이브 중요도 (Budget Save)", 0, 10, 5)

st.sidebar.markdown("---")
st.sidebar.header("🎯 3. 실시간 코스 변환 필터")
# ★[버그 해결의 키] 라디오 버튼 텍스트가 딕셔너리 키와 토씨 하나 안 틀리고 정확히 1:1 매칭됨
selected_concept = st.sidebar.radio(
    "원하는 코스 컨셉 선택 (클릭 즉시 아래 파트2 가변 반영)",
    ["스냅 사진 및 감성 출사", "완벽한 휴식과 신체 리커버리", "로컬 인프라 및 트렌디 쇼핑"]
)

# ==========================================
# [Part 1] 고도화된 스코어링 추천 알고리즘 가동 (인터락 마스터 키)
# ==========================================
st.subheader("📊 Part 1. 스펙 기반 AI 맞춤 추천 최적화 랭킹")

score_results = []
current_month_prices = df_flights.loc[user_month]

for dest in destinations:
    base = dest_base_scores[dest]
    
    # 1. 사용자의 목적 성향 분석 가중치 합산
    match_score = (base["휴양"] * w_relax) + (base["관광"] * w_sight) + (base["음식"] * w_food) + (base["쇼핑"] * w_shop)
    
    # 2. 예산 세이브 가중치 반영 (비용점수가 높은 비싼 국가는 패널티를 더 크게 부여하여 저비용 국가 순위 인상)
    budget_penalty = base["비용점수"] * w_budget
    
    # 3. 실시간 월별 항공료 변동 인덱스 페널티 계산
    price_penalty = (current_month_prices[dest] / df_flights[dest].max()) * 10
    
    # 4. 종합 최종 추천 점수 연산
    final_score = round(match_score - budget_penalty - price_penalty, 1)
    
    # [예산 쉴드 필터링 적용] 선택한 예산대 상한선을 넘으면 점수 디덕션 리스크 적용
    if user_budget_tier == "가성비 중심 (100만원 이하)" and base["비용점수"] > 4:
        final_score -= 50
    elif user_budget_tier == "적정 예산 (100만원 ~ 200만원)" and base["비용점수"] > 7:
        final_score -= 50
    elif user_budget_tier == "프리미엄·유럽형 (200만원 이상)" and base["비용점수"] < 6:
        final_score -= 15 # 고예산 유저는 먼 유럽이나 인프라 국가에 가중치 매칭 유도
    
    score_results.append({
        "국가 및 도시": dest,
        f"{user_month} 예상 항공료": f"{current_month_prices[dest]} 만원",
        "종합 매칭 신뢰도": final_score
    })

# 점수 기반 정렬 후 마스터 추천 국가 추출
df_ranking = pd.DataFrame(score_results).sort_values(by="종합 매칭 신뢰도", ascending=False).reset_index(drop=True)
st.dataframe(df_ranking, use_container_width=True)

top_1_destination = df_ranking.iloc[0]["국가 및 도시"]
st.success(f"🚀 **종합 추천 결론:** 입력 스펙 연산 결과, {user_month}에 [{trip_duration}]으로 떠나기 가장 최적화된 국가는 **[{top_1_destination}]** 입니다!")

# ==========================================
# [Part 2] 지도 및 가변 일수 타임라인 출력 (★실시간 15개 시나리오 락인 영역)
# ==========================================
st.markdown("---")
st.subheader(f"📍 Part 2. [{top_1_destination}] - [{selected_concept}] 동선 매핑 & 타임라인")
st.markdown("사이드바의 가중치를 바꾸면 국가가 변하고, 하단의 라디오 버튼을 바꾸면 **지도 맵 좌표와 일정이 실시간으로 100% 매칭 스위칭**됩니다.")

# ★완벽 버그 해결: 1위 국가와 라디오 선택 컨셉을 유기적으로 2중 인덱싱 결합
active_routing = concept_routing_matrix[top_1_destination][selected_concept]

