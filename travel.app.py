    day_keys = list(active_routing["days"].keys())
    sliced_day_keys = day_keys[:min(total_days, len(day_keys))]
    
    for day_key in sliced_day_keys:
        course_details = active_routing["days"][day_key]
        with st.expander(f"✨ {day_key}", expanded=True):
            for step in course_details:
                time_part, desc_part = step.split(" - ")
                st.markdown(f"**⏰ {time_part}** : {desc_part}")

